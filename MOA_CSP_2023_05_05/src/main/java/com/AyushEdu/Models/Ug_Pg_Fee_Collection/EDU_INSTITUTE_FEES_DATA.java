package com.AyushEdu.Models.Ug_Pg_Fee_Collection;

import static javax.persistence.GenerationType.IDENTITY;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;



@Entity
@Table(name = "edu_institute_fees_data", uniqueConstraints = {
@UniqueConstraint(columnNames = "id") })
public class EDU_INSTITUTE_FEES_DATA {
	
	
	private int id;
	private int stud_id;
	private double total_fees;
	private int paid_fees;
	private int institute_id;
	private int degree_id;
	private int prof_id;
	private int system_id;
	
	private String created_by;
	private Date created_date;
	private String modified_by;
	private Date modified_date;

	
	@Id
	@GeneratedValue(strategy = IDENTITY)
	@Column(name = "id", unique = true, nullable = false)
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getStud_id() {
		return stud_id;
	}
	public void setStud_id(int stud_id) {
		this.stud_id = stud_id;
	}
	public double getTotal_fees() {
		return total_fees;
	}
	public void setTotal_fees(double total_fees) {
		this.total_fees = total_fees;
	}
	public int getPaid_fees() {
		return paid_fees;
	}
	public void setPaid_fees(int paid_fees) {
		this.paid_fees = paid_fees;
	}
	public int getInstitute_id() {
		return institute_id;
	}
	public void setInstitute_id(int institute_id) {
		this.institute_id = institute_id;
	}
	public int getDegree_id() {
		return degree_id;
	}
	public void setDegree_id(int degree_id) {
		this.degree_id = degree_id;
	}
	public int getProf_id() {
		return prof_id;
	}
	public void setProf_id(int prof_id) {
		this.prof_id = prof_id;
	}
	public int getSystem_id() {
		return system_id;
	}
	public void setSystem_id(int system_id) {
		this.system_id = system_id;
	}
	public String getCreated_by() {
		return created_by;
	}
	public void setCreated_by(String created_by) {
		this.created_by = created_by;
	}
	public Date getCreated_date() {
		return created_date;
	}
	public void setCreated_date(Date created_date) {
		this.created_date = created_date;
	}
	public String getModified_by() {
		return modified_by;
	}
	public void setModified_by(String modified_by) {
		this.modified_by = modified_by;
	}
	public Date getModified_date() {
		return modified_date;
	}
	public void setModified_date(Date modified_date) {
		this.modified_date = modified_date;
	}
	
	
}
